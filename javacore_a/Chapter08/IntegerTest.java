public class IntegerTest {
    public static void main(String[] args) {
        int a1 = 32768;
        System.out.println("hello" + a1);

        long c = 1000000000000L;
        System.out.println(c);

        float f1 = 1.23f;
        System.out.println(f1);
        System.out.println((double)f1);
    }
}